# Metalinguistic Abstraction 

""" A powerful form of abstraction is to define a new language that is 
tailored to a particular type of application or problem domain.

Type of Application: Erlang was designed for concurrent programs. 
It has built-in elements for expressing concurrent communication. It is used,
for example, to implement chat servers

Problem Domain: The MediaWiki mark-up language was designed for generating static web pages.
It has built-in elements for text formatting and cross-page linking.

A programming language has:
Syntax: The legal statements and expressions in the language.
Semantics: The execution/evaluation rule for those statements and expressions.

To create a new programming language, you either need a:
Specificiation: A document describe the precise syntax and semantics of a language
Canonical Implementation: An interpreter or compiler for the language
"""

